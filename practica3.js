//Declarar variables
var numero = 0;

//Operadores arimaticos
for ( var num = 1; num<=100; num++); {
    //Metodo para verificar si el numero es par
    if (num % 2 ===0); {
        numero += num;
    }
    }

//Mostrar el resultado
document.write("La suma de los numero par del 1 al 100 es:",numero);
